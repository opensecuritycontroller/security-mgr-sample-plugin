package org.osc.manager.ism.model;


public class BaseIdNameObject {

    protected Long id;
    protected String name;

    public BaseIdNameObject(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id.toString();
    }

    @Override
    public String toString() {
        return "BaseIdNameObject [id=" + id + ", name=" + name + "]";
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + (id == null ? 0 : id.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (!(obj instanceof BaseIdNameObject)) {
            return false;
        }
        BaseIdNameObject other = (BaseIdNameObject) obj;
        if (id == null) {
            if (other.id != null) {
                return false;
            }
        } else if (!id.equals(other.id)) {
            return false;
        }
        return true;
    }

}
